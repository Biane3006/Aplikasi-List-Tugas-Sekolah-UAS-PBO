    private void tfSearchingKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfSearchingKeyTyped
        // TODO add your handling code here:
        int posisiy = 0;
        jPanel3.removeAll();
        try{
            String query = "SELECT * FROM tugasSekolah WHERE Judul LIKE ? OR MataPelajaran LIKE ? OR Deadline LIKE ? OR Prioritas LIKE ? OR Deskripsi LIKE ? AND UsernameAkun=?";
            PreparedStatement preparedStatement = Koneksi.getConnection().prepareStatement(query);
            preparedStatement.setString(1, "%"+tfSearching.getText()+"%");
            preparedStatement.setString(2, "%"+tfSearching.getText()+"%");
            preparedStatement.setString(3, "%"+tfSearching.getText()+"%");
            preparedStatement.setString(4, "%"+tfSearching.getText()+"%");
            preparedStatement.setString(5, "%"+tfSearching.getText()+"%");
            preparedStatement.setString(6, this.UsernameAkun);
            ResultSet resultSet = preparedStatement.executeQuery();
            jPanel3.setLayout(null);
            while(resultSet.next()){
                CardPanel cardBaru = new CardPanel(resultSet.getInt("Id"),resultSet.getString("Judul"),resultSet.getString("MataPelajaran"),resultSet.getDate("Deadline").toString(),resultSet.getString("Prioritas"),resultSet.getString("Deskripsi"),Boolean.parseBoolean(resultSet.getString("Complete")),UsernameAkun);
                cardBaru.setVisible(true);
                cardBaru.setBounds(0, posisiy, jPanel3.getWidth(), 90); // Menempatkan Panel1 dengan ukuran yang sama dengan Panel3
                jPanel3.add(cardBaru);
                posisiy = posisiy + cardBaru.getHeight() + 5;
            }
        }catch(SQLException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null,"Tidak Dapat Terhubung Ke Database","GAGAL",JOptionPane.ERROR_MESSAGE);
        }
        jPanel3.revalidate();
        jPanel3.repaint();
    }//GEN-LAST:event_tfSearchingKeyTyped

