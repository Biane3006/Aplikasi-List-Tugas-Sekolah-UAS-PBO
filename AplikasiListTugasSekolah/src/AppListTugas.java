                CardPanel cardBaru = new CardPanel(resultSet.getInt("Id"),resultSet.getString("Judul"),resultSet.getString("MataPelajaran"),resultSet.getDate("Deadline").toString(),resultSet.getString("Prioritas"),resultSet.getString("Deskripsi"),Boolean.parseBoolean(resultSet.getString("Complete")),UsernameAkun);
